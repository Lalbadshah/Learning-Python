import turtle

def drawsqr():
    window = turtle.Screen()
    window.bgcolor("red")
    
    bobby = turtle.Turtle()
    bobby.forward(100)
    bobby.right(90)
    bobby.forward(100)
    bobby.right(90)
    bobby.forward(100)
    bobby.right(90)
    bobby.forward(100)
    bobby.right(90)
    window.exitonclick()
drawsqr()
